/********************************************* DEBUG *****************************************************/
#ifdef	DEVICE_DEBUG

void DeBugMain(void);
#endif
/****************************************** Debug Ending *************************************************/

/**********************************************************************************************************
 @Function			int main(void)
 @Description			Main
 @Input				void
 @Return				int
**********************************************************************************************************/
int main(void)
{
	Stm32_Cache_Enable();												//打开L1-Cache
	HAL_Init();														//初始化HAL库
	Stm32_Clock_Init(432, 25, 2, 9);										//初始化时钟216Mhz 
	Delay_Init(216);													//初始化延时
	
	LED_Init();														//初始化LED
	
	while (1) {
		
#ifdef	DEVICE_DEBUG
		DeBugMain();
#endif
		
		
		
		
		
	}
}

#ifdef	DEVICE_DEBUG
/********************************************* DEBUG *****************************************************/

/****************************************** Debug Ending *************************************************/
/**********************************************************************************************************
 @Function			void DeBugMain(void)
 @Description			DeBugMain
 @Input				void
 @Return				void
**********************************************************************************************************/
void DeBugMain(void)
{
	Stm32_CalculagraphTypeDef CalTimer;
	
	while (1) {
		
		Stm32_Calculagraph_CountdownSec(&CalTimer, 2);
		while (Stm32_Calculagraph_IsExpiredSec(&CalTimer) != true);
		
		for (int i = 0; i < 6; i++) {
			LED0_Toggle();
			LED1_Toggle();
		
			Delay_MS(200);
		}
		
	}
}
#endif

